const calculadora = {
    soma(x,y){
        return x + y
    },
    subtrair(x,y){
        return x - y
    },
    multiplicar(x,y){
        return x * y
    },
    dividir(x,y){
        return x / y
    }

}

console.log(calculadora.soma(2,2))
console.log(calculadora.subtrair(4,2))
console.log(calculadora.multiplicar(5,2))
console.log(calculadora.dividir(6,2))